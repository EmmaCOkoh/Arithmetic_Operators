
x = 3.14
y = 4
z = 5

result = round(x) # round a number
print(result)

result1 = abs(y) # distance away from 0 as a whole number
print(result1)

result2 = pow(z, 2) # raised to the power
print(result2)

maximum_value = max(x, y, z) # maximum value
print(maximum_value)

minimum_value = min(x, y, z) # minimum value
print(minimum_value)

import math
print(math.pi) # pie
print(math.e) # exponential constant
print(math.sqrt(y)) # square root
print(math.ceil(x)) # round a floating number up
print(math.floor(x)) # round a number down


