friends = 4
# addition
friends = friends + 1
# or
friends += 1
print(friends)

# subtract
friends = friends - 1
# or
friends -= 1
print(friends)

# multiply
friends = friends * 1
# or
friends *= 1
print(friends)

# divide
friends = friends / 1
# or
friends /= 1
print(friends)

# modulus
friends = friends % 1
# or
friends %= 1
print(friends)

# remainder
friends = friends // 1
# or
friends //= 1
print(friends)

# power of 2
friends = friends ** 2
# or
friends **= 2
print(friends)
